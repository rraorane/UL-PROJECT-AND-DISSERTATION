# ---------------- STABILITY (2-MONTH) ----------------
@st.cache_data(show_spinner=False)
def compute_2m_volatility(close_prices: pd.DataFrame, days: int = 60) -> pd.Series:
    """
    2-month stability metric = volatility (std of daily returns) over last ~60 calendar days.
    Returns: Series index ISIN -> vol_60d
    """
    cp = close_prices.copy()
    cp["timestamp"] = pd.to_datetime(cp["timestamp"], errors="coerce")
    cp = cp.dropna(subset=["timestamp", "ISIN", "closePrice"])

    last_dt = cp["timestamp"].max()
    cutoff = last_dt - pd.Timedelta(days=days)

    cp = cp[cp["timestamp"] >= cutoff].sort_values(["ISIN", "timestamp"])
    cp["ret"] = cp.groupby("ISIN")["closePrice"].pct_change()
    vol_60d = cp.groupby("ISIN")["ret"].std()
    return vol_60d


# ---------------- INTERACTIONS ----------------
def prepare_interactions(tx: pd.DataFrame, allowed_customers=None):
    pos = tx[tx["transactionType"].astype(str).str.lower() == "buy"].copy()
    pos = pos.dropna(subset=["customerID", "ISIN", "totalValue"])

    if allowed_customers is not None:
        pos = pos[pos["customerID"].isin(allowed_customers)].copy()

    pos["weight"] = np.log1p(pos["totalValue"].clip(lower=0))

    user_ids = pd.Index(pos["customerID"].unique(), name="customerID")
    item_ids = pd.Index(pos["ISIN"].unique(), name="ISIN")

    u2i = {u: i for i, u in enumerate(user_ids)}
    i2i = {v: i for i, v in enumerate(item_ids)}

    pos["u"] = pos["customerID"].map(u2i)
    pos["i"] = pos["ISIN"].map(i2i)

    R = coo_matrix(
        (pos["weight"], (pos["u"], pos["i"])),
        shape=(len(user_ids), len(item_ids))
    ).tocsr()

    return pos, R, user_ids, item_ids, u2i, i2i


# ---------------- CONTENT FEATURES ----------------
def compute_content_features(assets: pd.DataFrame,
                            limit_prices: pd.DataFrame,
                            close_prices: pd.DataFrame,
                            item_ids: pd.Index):
    cp = close_prices.copy()
    cp["timestamp"] = pd.to_datetime(cp["timestamp"], errors="coerce")
    cp = cp.dropna(subset=["timestamp", "ISIN", "closePrice"])
    cp = cp.sort_values(["ISIN", "timestamp"])
    cp["ret"] = cp.groupby("ISIN")["closePrice"].pct_change()
    vol_all = cp.groupby("ISIN")["ret"].std().rename("volatility").reset_index()

    asset_feat = (
        assets.drop_duplicates("ISIN")
        .merge(limit_prices[["ISIN", "profitability"]], on="ISIN", how="left")
        .merge(vol_all, on="ISIN", how="left")
    )

    for col in ["profitability", "volatility"]:
        med = asset_feat[col].median()
        asset_feat[col] = asset_feat[col].fillna(med)

    cat_cols = ["assetCategory", "sector", "industry"]
    for c in cat_cols:
        if c not in asset_feat.columns:
            asset_feat[c] = "Unknown"

    oh = pd.get_dummies(asset_feat[cat_cols], dummy_na=True)

    num = asset_feat[["profitability", "volatility"]].astype(float)
    num_sc = pd.DataFrame(
        StandardScaler().fit_transform(num),
        columns=num.columns,
        index=asset_feat.index
    )

    X_item = pd.concat([num_sc, oh], axis=1)
    X_item["ISIN"] = asset_feat["ISIN"]
    X_item = X_item.set_index("ISIN").reindex(item_ids).fillna(0)
    return X_item


# ---------------- SIMILARITY ----------------
def prepare_similarity(R, X_item):
    item_vecs = R.T.tocsc()
    item_norms = np.sqrt(np.asarray(item_vecs.multiply(item_vecs).sum(axis=1)).ravel()) + 1e-9

    sims_content = cosine_similarity(X_item.values)
    np.fill_diagonal(sims_content, 0.0)
    return item_vecs, item_norms, sims_content


def top_similar_items_cf(item_vecs, norms, item_index, topn=100):
    v = item_vecs[item_index, :]
    v_norm = float(np.sqrt(v.multiply(v).sum())) + 1e-9

    num = np.asarray((item_vecs @ v.T).todense()).ravel()
    sims = np.divide(num, norms * v_norm, out=np.zeros_like(num), where=norms > 0)

    sims[item_index] = -1.0
    k = min(topn, len(sims) - 1)
    top_idx = np.argpartition(-sims, k)[:k]
    top_idx = top_idx[np.argsort(-sims[top_idx])]
    return top_idx, sims[top_idx]


# ---------------- SEGMENT MODEL BUILD (cached) ----------------
@st.cache_resource(show_spinner=False)
def build_segment_model(segment_key: str,
                        tx: pd.DataFrame,
                        customers: pd.DataFrame,
                        assets: pd.DataFrame,
                        limit_prices: pd.DataFrame,
                        close_prices: pd.DataFrame):
    """
    Build CF + CBF model only for customers in the same risk segment.
    """
    seg_customers = customers.copy()
    seg_customers["risk_norm"] = seg_customers["riskLevel"].apply(normalize_risk)

    allowed_customers = seg_customers.loc[
        seg_customers["risk_norm"] == segment_key, "customerID"
    ].unique()

    pos_s, R_s, user_ids_s, item_ids_s, u2i_s, i2i_s = prepare_interactions(tx, allowed_customers=allowed_customers)

    X_item_s = compute_content_features(assets, limit_prices, close_prices, item_ids_s)
    item_vecs_s, norms_s, sims_c_s = prepare_similarity(R_s, X_item_s)

    return {
        "pos": pos_s,
        "R": R_s,
        "user_ids": user_ids_s,
        "item_ids": item_ids_s,
        "u2i": u2i_s,
        "i2i": i2i_s,
        "X_item": X_item_s,
        "item_vecs": item_vecs_s,
        "norms": norms_s,
        "sims_c": sims_c_s,
    }


# ---------------- RECOMMENDER ----------------
def recommend_for_user(customer_id, alpha, topn,
                       customers, tx, assets, limit_prices, close_prices,
                       vol_60d: pd.Series,
                       strict_stability: bool = False):
    """
    Risk-segmented CF + 2-month stability gate + suitability filtering.
    Returns: rec_isins, reasons, risk, threshold
    """
    cust_row = customers[customers["customerID"] == customer_id].tail(1)
    risk = normalize_risk(cust_row["riskLevel"].values[0] if not cust_row.empty else None)

    model = build_segment_model(risk, tx, customers, assets, limit_prices, close_prices)

    R = model["R"]
    item_ids = model["item_ids"]
    u2i = model["u2i"]
    item_vecs = model["item_vecs"]
    norms = model["norms"]
    sims_content = model["sims_c"]

    thresholds = STABILITY_THRESHOLDS_STRICT if strict_stability else STABILITY_THRESHOLDS_RELAXED
    thresh = thresholds.get(risk, thresholds["Not Provided"])

    # Vol aligned to segment item universe
    v = vol_60d.reindex(item_ids)
    v_fill = v.fillna(v.median() if pd.notna(v.median()) else 0.0)

    # cold-start within the segment
    if customer_id not in u2i:
        seg_buy = model["pos"]
        pop = (seg_buy.groupby("ISIN")["totalValue"].sum().sort_values(ascending=False))
        recs = list(pop.index)

        # Apply stability gate + suitability for cold-start too
        recs = [r for r in recs if float(v_fill.get(r, v_fill.median())) <= thresh]

        allowed = RISK_TO_ALLOWED.get(risk, None)
        if allowed:
            cats = assets.drop_duplicates("ISIN").set_index("ISIN")["assetCategory"]
            recs = [r for r in recs if str(cats.get(r, "")).strip().title() in allowed]

        recs = recs[:topn]
        reasons = [f"Popular among {risk} customers (segment-based) + passed stability gate"] * len(recs)
        return recs, reasons, risk, thresh

    u = u2i[customer_id]
    owned = set(R[u].indices)

    # CF score (within segment)
    cf = np.zeros(len(item_ids))
    for i in owned:
        top_idx, sim = top_similar_items_cf(item_vecs, norms, i, topn=200)
        cf[top_idx] += sim

    # Content score
    cont = sims_content[list(owned)].mean(axis=0) if owned else np.zeros(len(item_ids))

    # Hybrid score
    score = alpha * cf + (1 - alpha) * cont
    score[list(owned)] = -np.inf

    # 2-month stability gate
    stable_mask = (v_fill <= thresh).values
    score = np.where(stable_mask, score, -np.inf)

    # MiFID suitability filter
    allowed = RISK_TO_ALLOWED.get(risk, None)
    if allowed:
        cats = assets.drop_duplicates("ISIN").set_index("ISIN")["assetCategory"].reindex(item_ids)
        mask = cats.astype(str).str.strip().str.title().isin(allowed).fillna(False).values
        score = np.where(mask, score, -np.inf)

    # Top-N
    valid = np.where(score > -np.inf)[0]
    if len(valid) == 0:
        return [], [], risk, thresh

    k = min(topn, len(valid))
    top_idx = np.argpartition(-score, k - 1)[:k]
    top_idx = top_idx[np.argsort(-score[top_idx])]
    rec_isins = list(item_ids[top_idx])

    reasons = [
        f"Segment-based ({risk}) similarity + passed 2-month stability gate + fits suitability policy."
        for _ in rec_isins
    ]
    return rec_isins, reasons, risk, thresh


# ---------------- LABEL HELPERS ----------------
def _wrap_label(s: str, width: int = 12, max_lines: int = 2) -> str:
    if not isinstance(s, str):
        return "N/A"
    words = " ".join(s.split())
    lines = textwrap.wrap(words, width=width)[:max_lines]
    if len(textwrap.wrap(words, width=width)) > max_lines:
        lines[-1] = lines[-1][:max(0, width - 1)] + "…"
    return "\n".join(lines)


def _customer_display(customers: pd.DataFrame, cid):
    row = customers.loc[customers["customerID"] == cid].head(1)
    if row.empty:
        return f"Customer {cid}", ""
    r = row.iloc[0].to_dict()

    name_parts = []
    for c in ["firstName", "first_name", "givenName", "given_name", "name"]:
        if c in r and pd.notna(r[c]) and str(r[c]).strip():
            name_parts.append(str(r[c]).strip())
            break
    for c in ["lastName", "last_name", "surname", "familyName", "family_name"]:
        if c in r and pd.notna(r[c]) and str(r[c]).strip():
            name_parts.append(str(r[c]).strip())
            break
    name = " ".join(name_parts) if name_parts else f"Customer {cid}"

    addr_parts = []
    for c in ["address", "street", "city", "state", "country", "postcode", "zip"]:
        if c in r and pd.notna(r[c]) and str(r[c]).strip():
            addr_parts.append(str(r[c]).strip())
    address = ", ".join(addr_parts)
    return name, address


