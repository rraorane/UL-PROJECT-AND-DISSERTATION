def normalize_risk(risk) -> str:
    if risk is None or (isinstance(risk, float) and np.isnan(risk)):
        return "Not Provided"
    r = str(risk).strip()
    if not r or r.lower() in {"nan", "none"}:
        return "Not Provided"
    r = r.title()
    fixes = {
        "Aggresive": "Aggressive",
        "Conservativ": "Conservative",
        "Income ": "Income",
    }
    return fixes.get(r, r)


